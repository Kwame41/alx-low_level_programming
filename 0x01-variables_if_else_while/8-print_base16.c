#include<stdio.h>
/**
 *main-Entry point
 *Description:print the hexadecimal numbesrs using lowercas&numbers.
 *the variables d,l for digits and lowercase respectively
 *Return:Always 0(Success)
 */
int main(void)
{
	int d;
	int l;

	for (d = 57; d <= 57; d++)
	{
		putchar(d);
	}
	for (l = 97; l <= 102; l++)
	{
		putchar(l);
	}
	putchar('\n');
	return (0);
}
